# Subtração
print(produto1 - produto2)

# Divisão
print(produto1 / produto2)

# Divisão inteira
print(produto1 // produto2)

# Multiplicação
print(produto1 * produto2)

# Modulo
print(produto1 % produto2)

# Exponenciação
print(produto1 ** produto2)
